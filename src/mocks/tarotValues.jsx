const files = import.meta.glob('../assets/images/cards/*.jpg', {
  eager: true,
  import: 'default',
});

const idToSrc = Object.fromEntries(
  Object.entries(files)
    .map(([path, src]) => {
      const m = path.match(/\/cards\/(\d+)\.jpg$/);
      return m ? [Number(m[1]), src] : null;
    })
    .filter(Boolean),
);

const tarotValues = Array.from({ length: 78 }, (_, i) => {
  const id = i + 1;
  return {
    id,
    title: titles[id] ?? '',
    image: idToSrc[id] ?? '',
  };
});

export const tarotById = Object.fromEntries(tarotValues.map((c) => [c.id, c]));
export default tarotValues;
